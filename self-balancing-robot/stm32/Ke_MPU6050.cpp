//初始化MPU6050
u8 MPU_Init(void)
{
	u8 res;
	//配置引脚PB7，控制MPU6050 AD0引脚，更改地址
  GPIO_InitTypeDef  GPIO_InitStructure;
	RCC_AHB1PeriphClockCmd(RCC_AHB1Periph_GPIOB,ENABLE);
	GPIO_InitStructure.GPIO_Pin = GPIO_Pin_7;	 
  GPIO_InitStructure.GPIO_Mode = GPIO_Mode_OUT;
  GPIO_InitStructure.GPIO_OType = GPIO_OType_PP;	
  GPIO_InitStructure.GPIO_Speed = GPIO_Speed_100MHz;
  GPIO_InitStructure.GPIO_PuPd = 	GPIO_PuPd_NOPULL;
  GPIO_Init(GPIOB, &GPIO_InitStructure);					 

	//配置PB7输出低电平，从机地址为0X68
	MPU_AD0_CTRL=0;			
	
	MPU_IIC_Init(); //IIC总线初始化
	MPU_Write_Byte(MPU_PWR_MGMT1_REG,0X80);	//复位MPU6050，重启内部寄存器到默认值 4.30
	Ke_Delay_Delayus(100);
	MPU_Write_Byte(MPU_PWR_MGMT1_REG,0X00);	//唤醒MPU6050 4.30
	MPU_Set_Gyro_Fsr(3);	// 配置 陀螺仪配置寄存器，设置满量程±2000dps  4.4
	MPU_Set_Accel_Fsr(0);	// 配置 加速度配置寄存器，设置满量程±2g				4.5
	MPU_Set_Rate(50);			// 配置 采样频率分频器，设置采样率为50Hz			4.2
	MPU_Write_Byte(MPU_INT_EN_REG,0X00);	//关闭所有中断  4.16
	MPU_Write_Byte(MPU_USER_CTRL_REG,0X00);	//I2C主模式关闭  4.29
	MPU_Write_Byte(MPU_FIFO_EN_REG,0X00);	  //关闭FIFO
	MPU_Write_Byte(MPU_INTBP_CFG_REG,0X80);	//INT引脚低电平有效  4.15
	res=MPU_Read_Byte(MPU_DEVICE_ID_REG);  //读取器件地址   4.25
	if(res==MPU_ADDR)
	{
		MPU_Write_Byte(MPU_PWR_MGMT1_REG,0X01);	 //设置x轴为参考时钟 4.30
		MPU_Write_Byte(MPU_PWR_MGMT2_REG,0X00);	 //唤醒全部设备  4.31
		MPU_Set_Rate(50);						
 	}
 	else return 1;
	return 0;
}

